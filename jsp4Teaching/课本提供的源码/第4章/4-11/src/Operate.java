public class Operate {
	public static void main(String[] args) {
		int i=0;
		System.out.println("使用i++的结果：");
		System.out.println(i++);
		System.out.println(i);
		i=0;
		System.out.println("使用++i的结果：");		
		System.out.println(++i);
		System.out.println(i);	
		i=1;
		System.out.println("使用i--的结果：");
		System.out.println(i--);
		System.out.println(i);
		i=1;
		System.out.println("使用--i的结果：");		
		System.out.println(--i);
		System.out.println(i);		
	}
}
