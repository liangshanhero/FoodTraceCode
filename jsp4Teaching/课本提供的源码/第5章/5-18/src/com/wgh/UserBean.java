package com.wgh;

public class UserBean {
	private String name; // 用户名
	private String pwd; // 密码

	// name属性对应的get方法
	public String getName() {
		return name;
	}

	// name属性对应的set方法
	public void setName(String name) {
		this.name = name;
	}

	// pwd属性对应的set方法
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	// pwd属性对应的get方法
	public String getPwd() {
		return pwd;
	}

}
